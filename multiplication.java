import java.io.*;
import java.util.*;
import java.lang.*;
public class multiplication
{
	public static void main(String args[])
	{
		int n;
		Scanner s=new Scanner(System.in);
		n=s.nextInt();
		for(int i=1;i<=10;i++)
		{
			System.out.println(n +"*"+i+"="+(n*i));
		}
	}
}
			









